// Вспомогательная функция для получения пояснения по названию критерия
export function getKutaExplanation(criterionName: string): KutaExplanation | undefined {
  return KUTA_EXPLANATIONS[criterionName];
}

// Функция для получения пояснений в зависимости от результата (благоприятный/неблагоприятный)
export function getExplanationForCriterion(
  criterionName: string, 
  isFavorable: boolean
): { benefits: string; challenges: string; recommendations: string } {
  const explanation = KUTA_EXPLANATIONS[criterionName];
  if (!explanation) {
    return {
      benefits: 'Информация отсутствует',
      challenges: 'Информация отсутствует',
      recommendations: 'Консультируйтесь с экспертом'
    };
  }
  
  return {
    benefits: isFavorable ? explanation.benefits.highScore : explanation.benefits.lowScore,
    challenges: isFavorable ? explanation.challenges.highScore : explanation.challenges.lowScore,
    recommendations: explanation.recommendations
  };
}